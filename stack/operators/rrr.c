#include "../../push_swap.h"

void    rrr(t_stack **a, t_stack **b)
{
    if (!a || !*a || !b || !*b)
        return ;
    reverse_rotate(a);
    reverse_rotate(b);
    ft_putstr_fd("rrr\n", 1);
}